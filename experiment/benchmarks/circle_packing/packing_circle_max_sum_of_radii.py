  avg_r_estimate = 1.0 / (2.0 * np.sqrt(n)) if n > 0 else 0.5
  # Slightly smaller base steps, as we expect to be near optimum.
  stepsize_pos = avg_r_estimate * 0.05
  stepsize_rad = avg_r_estimate * 0.025
