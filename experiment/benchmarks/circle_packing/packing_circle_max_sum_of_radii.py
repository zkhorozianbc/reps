  # --- Optimization Setup ---

  # Objective function: minimize negative sum of radii
  def objective_bh(vars):
    # vars = [x1, y1, r1, x2, y2, r2, ...]
    radii = vars[2::3]
    return -np.sum(radii)

  # Gradient of the objective function
  def jac_objective_bh(vars):
    # Objective: -sum(r_i)
    N_vars = len(vars)
    grad = np.zeros(N_vars)
    # d/dr_i = -1
